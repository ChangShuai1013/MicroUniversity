package com.changshuai.model;

public class Shop {
	public String shop_id ;
	public String shop_name  ;
	public String shop_pic ;
	public String shop_description ;
	public String shop_price ;
	public String shop_discount_price ;
	public  String shop_count ;
	
	public String getShop_id() {
		return shop_id;
	}
	public void setShop_id(String shop_id) {
		this.shop_id = shop_id;
	}
	public String getShop_name() {
		return shop_name;
	}
	public void setShop_name(String shop_name) {
		this.shop_name = shop_name;
	}
	public String getShop_pic() {
		return shop_pic;
	}
	public void setShop_pic(String shop_pic) {
		this.shop_pic = shop_pic;
	}
	public String getShop_description() {
		return shop_description;
	}
	public void setShop_description(String shop_description) {
		this.shop_description = shop_description;
	}
	public String getShop_price() {
		return shop_price;
	}
	public void setShop_price(String shop_price) {
		this.shop_price = shop_price;
	}
	public String getShop_discount_price() {
		return shop_discount_price;
	}
	public void setShop_discount_price(String shop_discount_price) {
		this.shop_discount_price = shop_discount_price;
	}
	
}
